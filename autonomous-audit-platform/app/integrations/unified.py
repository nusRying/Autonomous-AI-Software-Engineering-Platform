class GitLabIntegrator:
    """Pillar #1, #2: Code Control & Permissions (GitLab CE)"""
    def __init__(self):
        import gitlab
        self.url = "http://localhost:8080"
        # In a real setup, this would come from an ENV variable or vault
        self.token = os.getenv("GITLAB_PRIVATE_TOKEN", "MOCK_TOKEN")
        try:
            self.gl = gitlab.Gitlab(self.url, private_token=self.token)
        except Exception:
            self.gl = None

    async def create_repository(self, name: str):
        logger.info(f"Pillar #1: Creating new GitLab repository: {name}")
        if not self.gl: return {"status": "MOCK_MODE"}
        project = self.gl.projects.create({'name': name})
        return project.attributes

    async def create_merge_request(self, project_id: str, source_branch: str, target_branch: str, title: str):
        logger.info(f"Pillar #1: Creating GitLab Merge Request for {project_id}...")
        if not self.gl: return {"status": "MR_CREATED", "mr_url": f"http://gitlab.local/projects/{project_id}/merge_requests/1"}
        
        project = self.gl.projects.get(project_id)
        mr = project.mergerequests.create({
            'source_branch': source_branch,
            'target_branch': target_branch,
            'title': title
        })
        return mr.attributes

    async def get_audit_trail(self, user_id: str):
        logger.info(f"Pillar #2: Fetching GitLab Audit logs for user {user_id}...")
        if not self.gl: return [{"action": "push", "timestamp": str(datetime.now()), "ref": "refs/heads/main"}]
        # Fetching events for the user
        events = self.gl.events.list(all=True)
        return events

